# Numbers
myint = 7
print ("Just an int:", myint)
myfloat = 7.2
print ("Float has decimals:", myfloat)
myfloat = float(7)
print ("Float can also be explicit with float(x):", myfloat)

# Strings
mystring = 'Double quotes can easily use apostrophes.'
print (mystring)
mystring = "Don't try this with single quotes."
print (mystring)
mystring = 'Escape characters aren\'t out of the question.'
print (mystring)
mystring = r"\n means newline, be careful with paths."
print (mystring)

x, y = 4, 5
print ("You can even assign many things at once:", x, y)